// コミュニティ掲示板へ投稿するときの下書きテキストを作る
export function buildCommunityMessage(record: RecordItem) {
  return `「${record.theme}」について振り返りました。${record.insight}`;
}

export function buildCommunityLineShareText(post: SharedPost) {
  return [
    `【${post.nickname}さんの振り返り／テーマ：${post.theme}】`,
    post.insight,
    "",
    "【明日の一歩】",
    post.tomorrowAction
  ].join("\n");
}

// コミュニティ掲示板に集まった投稿を、クロードに読んでもらってまとめ・コメントしてもらうためのプロンプト。
// 今はコピペで一往復する形（将来は自動でクロードが返信を返す仕組みに発展させる予定）。
export function buildCommunityReviewPrompt(posts: SharedPost[]) {
  const postsText = posts
    .map((post, index) => {
      return [
        `${index + 1}. ${post.nickname}さん（テーマ：${post.theme}）`,
        `気づき：${post.insight || "（未記入）"}`,
        `明日の一歩：${post.tomorrowAction || "（未記入）"}`
      ].join("\n");
    })
    .join("\n\n");

  return [
    "以下は、TSUNAGU DAO人間力向上委員会のメンバーが今日シェアした振り返りの一覧です。",
    "",
    "人間力向上委員会の視点（ご機嫌・かっこいい大人・4学期の循環）から、みんなの投稿を温かく読んで、次の3つをまとめてください。",
    "",
    "1. みんなの投稿に共通して見える気づきやテーマ傾向",
    "2. 特に良かった視点や、他のメンバーの参考になりそうな一歩を1〜2件ピックアップして紹介",
    "3. 明日に向けて、みんなへ向けた短い応援コメント",
    "",
    "【今日の投稿一覧】",
    postsText || "（まだ投稿がありません）"
  ].join("\n");
}
